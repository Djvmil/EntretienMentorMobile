/** Light Android background theme */
val LightAndroidBackgroundTheme = BackgroundTheme(color = Color.DarkGray)

/** Dark Android background theme */
val DarkAndroidBackgroundTheme = BackgroundTheme(color = Color.Black)

@Composable
fun EntretienMentorTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    useAndroidTheme: Boolean = false,
    content: @Composable () -> Unit
) =
    EntretienMentorTheme(
        useDarkTheme = useDarkTheme,
        useAndroidTheme = useAndroidTheme,
        disableDynamicTheming = false,
        content = content)

@Composable
internal fun EntretienMentorTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    useAndroidTheme: Boolean = true,
    // Dynamic color is available on Android 12+
    disableDynamicTheming: Boolean = true,
    content: @Composable () -> Unit
) {

  val colorScheme =
      if (useAndroidTheme) {
        if (useDarkTheme) DarkAndroidColors else LightAndroidColors
      } else if (!disableDynamicTheming && supportsDynamicTheming()) {
        val context = LocalContext.current
        if (useDarkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
      } else {
        if (useDarkTheme) DarkColors else LightColors
      }

  val defaultGradientColors = GradientColors()
  val gradientColors =
      if (useDarkTheme || (!disableDynamicTheming && supportsDynamicTheming())) {
        defaultGradientColors
      } else {
        if (useDarkTheme) defaultGradientColors else LightDefaultGradientColors
      }

  val defaultBackgroundTheme = BackgroundTheme(color = colorScheme.surface, tonalElevation = 2.dp)

  val backgroundTheme =
      if (useAndroidTheme) {
        if (useDarkTheme) DarkAndroidBackgroundTheme else LightAndroidBackgroundTheme
      } else {
        defaultBackgroundTheme
      }

  val view = LocalView.current
  if (!view.isInEditMode) {
    SideEffect {
      val window = (view.context as Activity).window
      window.statusBarColor = colorScheme.primary.toArgb()
      WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = useDarkTheme
    }
  }

  CompositionLocalProvider(
      LocalGradientColors provides gradientColors, LocalBackgroundTheme provides backgroundTheme) {
        MaterialTheme(
            colorScheme = colorScheme, typography = typography, shapes = Shapes, content = content)
      }
}

@ChecksSdkIntAtLeast(api = Build.VERSION_CODES.S)
private fun supportsDynamicTheming() = Build.VERSION.SDK_INT >= Build.VERSION_CODES.S
